// ─────────────────────────────────────────────────────────────────────────
// Icon / illustrated-asset manifest generator.
//
// Walks `public/icons/**` (the servable illustrated + 3D + animated packs) and
// emits a typed TS module `src/icons/asset-manifest.ts` — the single source of
// truth that every Ironflyer app can map over for feature tiles, empty states,
// onboarding, and marketing visuals. Re-run after adding assets:
//
//     node scripts/gen-icon-manifest.mjs
//
// UI *control* glyphs are NOT here — those live in `src/icons/index.tsx`
// (Lucide). This manifest is strictly the illustrated/animated library.
// ─────────────────────────────────────────────────────────────────────────
import { readdirSync, statSync, writeFileSync } from 'node:fs';
import { join, relative, extname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));
const iconsDir = join(root, 'public', 'icons');
const outFile = join(root, 'src', 'icons', 'asset-manifest.ts');

const KIND_BY_EXT = {
  '.svg': 'svg',
  '.png': 'png',
  '.gif': 'animated', // looping raster
  '.mp4': 'animated', // looping 3D video
  '.webp': 'png',
  '.glb': 'model3d',
  '.fbx': 'model3d',
  '.obj': 'model3d',
};

// Generic container / format folders carry no semantic meaning — skip them so
// the pack id comes from the most specific *named* folder.
const GENERIC = new Set([
  'all-icons', 'svg', 'png', 'png files', 'png-files', '11',
  'fbx', 'glb', 'obj', 'gltf', 'assets', 'v1', 'v2', 'lottie', 'json',
]);

// Hand tuned ids for the known top folders so the typed keys read cleanly.
const ALIAS = {
  svg: 'chat', // public/icons/SVG — chat/messaging line set
  11: 'chatbot', // public/icons/11 — AI chatbot line set
  '3d-animated-icons': 'animated-3d',
  'insurance-3d-icons': 'insurance-3d',
  'digital-marketing-3d-icon': 'marketing-3d',
  'marketing-strategy-3d-icons': 'strategy-3d',
  'smooth-banking-3d-icon': 'banking-3d',
  'seo-2': 'seo',
  png: 'security', // all-icons/PNG — flat security/access icon set
  'png-files': 'infographic', // all-icons/PNG Files — infographic pieces
};

const norm = (segment) =>
  segment
    .toLowerCase()
    .replace(/-?\d{4}-\d{2}-\d{2}.*$/, '') // strip trailing date-stamps
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');

const isGeneric = (d) => GENERIC.has(norm(d));
const aliasOrNorm = (d) => ALIAS[norm(d)] ?? norm(d);

// Pick the SHALLOWEST *named* folder (skipping generic/format/version buckets)
// so a deeply-nested asset rolls up to its top-level pack, not a stray subdir.
const PACK_ID = (segs) => {
  const dirs = segs.slice(0, -1); // drop the filename
  for (const d of dirs) if (!isGeneric(d)) return aliasOrNorm(d);
  // All segments are generic → use an explicit alias on the first, else last.
  const first = dirs[0] ?? '';
  return ALIAS[norm(first)] ?? aliasOrNorm(dirs[dirs.length - 1] ?? 'core');
};

const slug = (name) =>
  name
    .toLowerCase()
    .replace(/\.[^.]+$/, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');

function walk(dir, acc = []) {
  for (const entry of readdirSync(dir)) {
    if (entry.startsWith('.')) continue;
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) walk(full, acc);
    else acc.push(full);
  }
  return acc;
}

const files = walk(iconsDir).filter((f) => KIND_BY_EXT[extname(f).toLowerCase()]);

const seen = new Set();
const entries = files
  .map((full) => {
    const rel = relative(iconsDir, full); // e.g. "all-icons/SEO 2/x.svg"
    const segs = rel.split('/');
    const pack = PACK_ID(segs);
    const kind = KIND_BY_EXT[extname(full).toLowerCase()];
    const name = basename(full).replace(/\.[^.]+$/, '');
    let id = `${pack}/${slug(name)}`;
    let n = 2;
    while (seen.has(id)) id = `${pack}/${slug(name)}-${n++}`;
    seen.add(id);
    // Served from the web root (vite serves public/ at "/").
    const path = '/' + rel.split('/').map(encodeURIComponent).join('/');
    return { id, name, pack, kind, path };
  })
  .sort((a, b) => a.id.localeCompare(b.id));

const packs = [...new Set(entries.map((e) => e.pack))].sort();

const header = `// AUTO-GENERATED by scripts/gen-icon-manifest.mjs — DO NOT EDIT BY HAND.
// Single source of truth for servable illustrated / 3D / animated assets under
// public/icons. Consume via src/icons/assets.ts (typed accessor + groups).
// Re-run: node scripts/gen-icon-manifest.mjs
/* eslint-disable */
`;

const body = `export type AssetKind = 'svg' | 'png' | 'animated' | 'model3d';
export type AssetPack = ${packs.map((p) => JSON.stringify(p)).join(' | ')};
export interface AssetEntry {
  /** stable "pack/name" id */
  id: string;
  /** human label from the source filename */
  name: string;
  pack: AssetPack;
  kind: AssetKind;
  /** web path served from public/ (already URL-encoded) */
  path: string;
}

export const ASSET_PACKS = ${JSON.stringify(packs, null, 2)} as const;

export const ASSET_MANIFEST: AssetEntry[] = ${JSON.stringify(entries, null, 2)};
`;

writeFileSync(outFile, header + '\n' + body, 'utf8');

const byKind = entries.reduce((m, e) => ((m[e.kind] = (m[e.kind] || 0) + 1), m), {});
console.log(`icon manifest → ${relative(root, outFile)}`);
console.log(`  ${entries.length} assets · ${packs.length} packs · ${JSON.stringify(byKind)}`);
console.log(`  packs: ${packs.join(', ')}`);
