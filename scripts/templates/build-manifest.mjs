#!/usr/bin/env node
// Scan templates/sites/<category>/<slug>/template.json and emit a single
// manifest.json + a TypeScript module the web gallery imports.
//
// Run from repo root: `node scripts/templates/build-manifest.mjs`

import { readFileSync, readdirSync, statSync, writeFileSync, existsSync } from 'node:fs';
import { join, resolve, relative } from 'node:path';

const REPO_ROOT = resolve(new URL('.', import.meta.url).pathname, '..', '..');
const SITES_ROOT = join(REPO_ROOT, 'templates', 'sites');
const MANIFEST_OUT = join(SITES_ROOT, 'manifest.json');
const TS_OUT = join(REPO_ROOT, 'apps', 'web', 'app', 'app', 'resources', 'templates.generated.ts');

function isDir(p) { try { return statSync(p).isDirectory(); } catch { return false; } }

function readJSON(p) {
  try { return JSON.parse(readFileSync(p, 'utf8')); }
  catch (e) { throw new Error(`Failed parsing ${p}: ${e.message}`); }
}

function scanCategory(category) {
  const dir = join(SITES_ROOT, category);
  if (!isDir(dir)) return [];
  const slugs = readdirSync(dir).filter((n) => isDir(join(dir, n))).sort();
  const out = [];
  for (const slug of slugs) {
    const slugDir = join(dir, slug);
    const metaPath = join(slugDir, 'template.json');
    const htmlPath = join(slugDir, 'index.html');
    if (!existsSync(metaPath)) {
      console.warn(`skip ${category}/${slug} — no template.json`);
      continue;
    }
    if (!existsSync(htmlPath)) {
      console.warn(`skip ${category}/${slug} — no index.html`);
      continue;
    }
    const meta = readJSON(metaPath);
    out.push({
      ...meta,
      source: `templates/sites/${category}/${slug}`,
      categoryDir: category,
      index: `templates/sites/${category}/${slug}/index.html`,
    });
  }
  return out;
}

const CATEGORIES = [
  'saas', 'agency', 'portfolio', 'ecommerce', 'restaurant',
  'realestate', 'fitness', 'education', 'blog', 'mobile-app',
  'events', 'beauty', 'travel', 'nonprofit', 'web3',
];

const allTemplates = [];
const perCategory = {};
for (const cat of CATEGORIES) {
  const items = scanCategory(cat);
  perCategory[cat] = items.length;
  allTemplates.push(...items);
}

const manifest = {
  generatedAt: new Date().toISOString(),
  totalCount: allTemplates.length,
  categories: perCategory,
  templates: allTemplates,
};

writeFileSync(MANIFEST_OUT, JSON.stringify(manifest, null, 2) + '\n', 'utf8');
console.log(`wrote ${relative(REPO_ROOT, MANIFEST_OUT)} — ${allTemplates.length} templates`);
for (const [cat, n] of Object.entries(perCategory)) {
  console.log(`  ${cat.padEnd(14)} ${n}`);
}

// Emit a TypeScript module the gallery imports directly so we avoid runtime
// fetches and keep static analysis intact.
const tsHeader =
`// AUTO-GENERATED by scripts/templates/build-manifest.mjs.
// Source of truth lives in templates/sites/<category>/<slug>/template.json.
// Do not edit by hand — re-run the script.

export interface TemplateMeta {
  slug: string;
  title: string;
  subtitle: string;
  category: string;
  type: 'Apps' | 'Websites' | 'Commerce' | 'Mobile/PWA';
  tags: string[];
  previewImage: string;
  livePreview?: string;
  palette: { bg: string; fg: string; accent: string };
  sections: string[];
  stack: string;
  prompt: string;
  source: string;
  categoryDir: string;
  index: string;
}

export const TEMPLATE_COUNT = ${allTemplates.length};

export const TEMPLATES: TemplateMeta[] = ${JSON.stringify(allTemplates, null, 2)};

export const TEMPLATES_BY_CATEGORY: Record<string, number> = ${JSON.stringify(perCategory, null, 2)};
`;

writeFileSync(TS_OUT, tsHeader, 'utf8');
console.log(`wrote ${relative(REPO_ROOT, TS_OUT)}`);
